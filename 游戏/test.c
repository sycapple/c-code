void game()
{
	char ret = '0';
	int i, j;
	char board[ROW][COL] = { 0 };
	InitBoard(board, ROW, COL);//初始化
	DisplayBoard(board, ROW, COL);//打印棋盘
	while (1)
	{
		//玩家下棋
		PlayerMove(board,ROW,COL);
		DisplayBoard(board, ROW, COL);//打印棋盘
		ret = Judge(board, ROW, COL);
		if (ret != 'C')
		{
			break;
		}
		//电脑下棋
		ComputerMove(board, ROW, COL);
		DisplayBoard(board, ROW, COL);//打印棋盘
		ret = Judge(board, ROW, COL);
		if (ret != 'C')
		{
			break;
		}
	}
	if (ret == '*')
	{
		printf("玩家胜利\n");
	}
	else if(ret=='#')
	{
		printf("电脑胜利\n");
	}
	else
	{
		printf("平局！\n");
	}

}
